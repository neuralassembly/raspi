import smbus
from time import sleep

bus = smbus.SMBus(1)

# initializing
bus.write_i2c_block_data(0x3e, 0x00, [0x38, 0x39, 0x14, 0x70, 0x56, 0x6c])   # often stops at "0x6e"
sleep(0.2)

bus.write_i2c_block_data(0x3e, 0x00, [0x38, 0x0d, 0x01])
sleep(0.001)


# writing characters (often stops somewhere in "Hello World")
bus.write_byte_data(0x3e, 0x40, 0x48) # H
bus.write_byte_data(0x3e, 0x40, 0x65) # e
bus.write_byte_data(0x3e, 0x40, 0x7c) # l
bus.write_byte_data(0x3e, 0x40, 0x7c) # l
bus.write_byte_data(0x3e, 0x40, 0x6f) # o
bus.write_byte_data(0x3e, 0x40, 0x20) # 
bus.write_byte_data(0x3e, 0x40, 0x57) # W
bus.write_byte_data(0x3e, 0x40, 0x6f) # o

bus.write_byte_data(0x3e, 0x00, 0xc0) # (newline) # often stops at "0xc0"

bus.write_byte_data(0x3e, 0x40, 0x72) # r
bus.write_byte_data(0x3e, 0x40, 0x7c) # l
bus.write_byte_data(0x3e, 0x40, 0x64) # d


